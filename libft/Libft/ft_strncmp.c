#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*st1;
	unsigned char	*st2;

	i = 0;
	st1 = (unsigned char *)s1;
	st2 = (unsigned char *)s2;
	while (st1[i] == st2[i] && st1[i] != '\0' && i < n)
		i++;
	if (i == n)
		return (0);
	return (st1[i] - st2[i]);
}
