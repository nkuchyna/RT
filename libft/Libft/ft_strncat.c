#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	int		j;
	size_t	k;
	char	*a;

	a = (char *)s2;
	i = 0;
	j = 0;
	k = 0;
	while (s1[i] != '\0')
		i++;
	while (a[j] != '\0' && k < n)
	{
		s1[i] = a[j];
		i++;
		j++;
		k++;
	}
	s1[i] = '\0';
	return (s1);
}
